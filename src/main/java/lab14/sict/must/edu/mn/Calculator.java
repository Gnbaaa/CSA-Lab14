package lab14.sict.must.edu.mn;

/**
 * Энгийн тооны машины үндсэн класс.
 */
public class Calculator {
    /**
     * Хоёр тоог нэмнэ.
     *
     * @param a Эхний тоо
     * @param b Хоёр дахь тоо
     * @return Тоонуудын нийлбэр
     */
    public double add(double a, double b) {
        return a + b;
    }

    /**
     * Хоёр тоог хасах.
     *
     * @param a Эхний тоо
     * @param b Хоёр дахь тоо
     * @return Тоонуудын ялгавар
     */
    public double subtract(double a, double b) {
        return a - b;
    }
}

